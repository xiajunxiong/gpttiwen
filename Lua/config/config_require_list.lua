function ConfigRequireSelect(cfg_path_list)
	local base_spid = GLOBAL_CONFIG:BaseSpId()
	if DEBUG_MODE then
		--UnityEngine.Debug.LogError("专服id:" .. (base_spid or ""))
	end
	if base_spid == nil or base_spid == "" or base_spid == "dev" then
		return cfg_path_list
	end
	local cfg = Config.randactivity_spid_auto
	if not cfg[base_spid] or not next(cfg[base_spid]) then
		return cfg_path_list
	end
	local child_cfg
	for i,v in pairs(cfg.spid) do
		if v.spid == base_spid then
			child_cfg = v
			break
		end
	end
	if not child_cfg then
		return cfg_path_list
	end

	local t_list = {}
	for i,v in pairs(cfg[base_spid]) do
		local k = v.filename .. "_auto"
		t_list[k] = true
	end
	for i,v in pairs(cfg_path_list) do
		local split = string.split(v, "/")
		local file_name = split[#split]
		if t_list[file_name] then
			local new_path = ""
			for j = 1, #split - 1 do
				new_path = new_path .. split[j] .. "/"
			end
			local new_file_name = child_cfg.ra_cfg_prefix .. "_" .. file_name
			local folder = child_cfg.ra_cfg_directory ~= "" and child_cfg.ra_cfg_directory .. "/" or ""
			new_path = string.format("%s%s%s", new_path, folder, new_file_name)
			cfg_path_list[i] = new_path
			if DEBUG_MODE then
				UnityEngine.Debug.LogError(string.format("专服%s 引用新配置：%s", base_spid, new_path))
			end
		end
	end
	return cfg_path_list
end

