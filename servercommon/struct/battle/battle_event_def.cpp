#include "battle_event_def.hpp"

UInt16 BATTLE_EVENT_VERSION[BATTLE_EVENT_MAX];

void BattleEventVersionInit()
{
	memset(BATTLE_EVENT_VERSION, 0, sizeof(BATTLE_EVENT_VERSION));

	//协议事件有新版本更替后需要版本号+1

	//比如BATTLE_EVENT_PERFORM_SKILL有改动
	BATTLE_EVENT_VERSION[BATTLE_EVENT_MODIFY_HP] = 1;
	BATTLE_EVENT_VERSION[BATTLE_EVENT_SUMMON_BATTLE_CHARACTER] = 1;
	BATTLE_EVENT_VERSION[BATTLE_EVENT_MONSTER_TRANSFORM] = 1;
}